my_dict = {
    "vrunda": 75,
    "akul": 89,
    "muskan": 52,
}
# 1. .keys()
print(my_dict.keys())  # Returns list of keys.
# 2. .values()
print(my_dict.values())  # Returns list of values.
# 3. .items()
print(my_dict.items())  # Returns list of key-value tuples.
# 4. Len()
print(len(my_dict))  # Returns number of key-value pairs in the dict.
