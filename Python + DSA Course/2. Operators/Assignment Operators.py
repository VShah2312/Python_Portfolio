# Assignment Operators

x = 4

# To as sign value:

x += 4  # x=x+4
print(x)  # Combined with Arithmetic operators  Answer =8
x -= 4  # x=x-4
print(x)  # Answer =4
x *= 4  # x=x*4
print(x)  # Answer =16
x /= 4  # x=x/4
print(x)  # Answer =4.0
x %= 4  # x=x%4
print(x)  # Answer=0.0
x = 2
x **= 4  # x=x**4
print(x)  # answer=16
x //= 4  # x=x//4
print(x)  # Answer=4
