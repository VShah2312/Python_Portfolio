# ArithmetiC Operators
# Addition (+), Subtraction (-), Multiplication (*), Divison (/), Modulo(%), Exponent (**), Floor Division(//)
print(10 % 10)  # Answer=0
print(20 % 3)  # Answer= 2
print(5 % 10)  # Answer= 5 python returns small value when modulo is not possible
print(9 % 10)  # Answer= 9
print(-16 % 5)  # Answer= 4

print(5**0)
print(5**-1)  # Answer= 0.2


print(10 / 4)  # Answer =2.5 Answers is always in float
print(10 // 4)  # Answer=2 Answer is always in int. It already takes floor value
print(13 // 7)  # Answer = 1
print(-10 // 4)  # Answer =-3
